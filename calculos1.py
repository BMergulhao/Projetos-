#Exercício: Pede o numero (float) ao utilizador e que exibe:
# Dobro
# Triplo
# Quadrado
# Cubo
# Resultado de 2,5*x + 10, onde x é o numero de pedido do utilizador:

num = float(input ("Introduza um número: "))
print("Dobro      : " , 2 * num)
print("Triplo     : " , 3 * num)
print("Quadrado   : " , num ** 2)
print("Cubo       : " , num ** 3)
print("2.5*x + 10 : " , 2.5 * num + 10)


